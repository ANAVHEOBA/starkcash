/// Apply Feistel-like round function
fn feistel_round(left: &mut [u8; 16], right: &mut [u8; 16], round_key: &[u8; 32]) {
    // XOR left with round function of right
    for i in 0..16 {
        left[i] ^= right[i].wrapping_add(round_key[i]);
        left[i] = left[i].rotate_left(3);
    }
}

/// MiMC7-inspired hash function with 81 rounds
/// Provides strong collision resistance for testing
pub fn mimc7_hash(left: &[u8], right: &[u8]) -> [u8; 32] {
    assert_eq!(left.len(), 32, "Input must be 32 bytes");
    assert_eq!(right.len(), 32, "Input must be 32 bytes");

    // Split inputs into halves for Feistel network
    let mut left_half = [0u8; 16];
    let mut right_half = [0u8; 16];
    left_half.copy_from_slice(&left[0..16]);
    right_half.copy_from_slice(&left[16..32]);

    let mut right_input = [0u8; 16];
    right_input.copy_from_slice(&right[0..16]);

    // 81 rounds of Feistel with different keys
    for round in 0..81 {
        // Create round key from ROUND_KEYS and right input
        let mut round_key = [0u8; 32];
        for i in 0..32 {
            round_key[i] = ROUND_KEYS[round][i].wrapping_add(right[i]);
        }

        // Apply round
        feistel_round(&mut left_half, &mut right_half, &round_key);

        // Swap halves
        std::mem::swap(&mut left_half, &mut right_half);

        // Additional mixing every 7 rounds
        if round % 7 == 0 {
            for i in 0..16 {
                left_half[i] = left_half[i].wrapping_mul(right_input[i] | 1);
                right_half[i] = right_half[i].rotate_left((round % 8 + 1) as u32);
            }
        }
    }

    // Final swap if odd number of rounds
    if 81 % 2 == 1 {
        std::mem::swap(&mut left_half, &mut right_half);
    }

    // Combine halves with right input for final mixing
    let mut result = [0u8; 32];
    result[0..16].copy_from_slice(&left_half);
    result[16..32].copy_from_slice(&right_half);

    // Final XOR with right input for binding
    for i in 0..32 {
        result[i] ^= right[i];
    }

    result
}
